package chapter1;

public class Step2 {

	public static void main(String[] args) {
		System.out.println("안녕"); // 문자열은 쌍따옴표 필요

		System.out.println(12); // 숫자 자료형
		System.out.println(-34);
		System.out.println(3.14);

		// boolean 자료형
		System.out.println(true); // 참
		System.out.println(false); // 거짓

		System.out.println(123 + 456); // 자동 연산
	}
}